#include <stdio.h>
#include <stdlib.h>

int main() {
    int a = 5, b = 10;

    printf("\n\n--- Operadores de Comparação (Relacionais) ---\n\n");

    printf("a = %d, b = %d\n\n", a, b);

    printf("a == b? %d\n", a == b);

    printf("a != b? %d\n", a != b);

    printf("a > b? %d\n", a > b);

    printf("a < b? %d\n", a < b);

    printf("a >= b? %d\n", a >= b);

    printf("a <= b? %d\n", a <= b);

    printf("\n\n");

    return 0;
}
